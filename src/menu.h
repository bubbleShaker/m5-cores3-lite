// メニューのカーソル移動の純粋ロジック（Issue #132）。
// 実シーンや描画には依存せず、「現在のカーソル位置」「項目数」「移動量」だけで
// 次のカーソル位置を決める。next_scene（巡回）と違い、メニューは端で止める（クランプ）:
// リスト UI では末尾からさらに下へ行っても先頭へ飛ばない方が予測しやすいため。

// カーソルを delta（-1=上 / +1=下 など）だけ動かした先の位置を返す純粋関数。
//   count <= 0 のときは 0 を返す（項目なし時の安全値）。
//   結果は [0, count) にクランプする（端を越えない）。
//   current が範囲外でも [0, count) に丸めてから動かす（堅牢性）。
int menu_move(int current, int count, int delta);
