// シーン巡回の純粋ロジック（テーマ M / Issue #33）。
// 実シーンの種類（Face/Sheep/Art…）には依存せず、「現在の番号」と「総数」だけで
// 次の番号を決める。これにより、シーンを増やしてもこのロジックは無改変で済む。

// 長押しで次のシーンへ進む時の番号を返す純粋関数。
//   最後のシーンの次は先頭へ折り返す（巡回）。
//   count <= 0 のときは 0 を返す（シーン無し時の安全値）。
//   current が範囲外でも [0, count) に正規化してから進める（堅牢性）。
int next_scene(int current, int count);
